/**
 * Задача 2
 *
 * Путник двигался t1 часов со скоростью v1 км/час, затем t2 часов — со скоростью v2 км/час
 * и t3 часов — со скоростью v3 км/час.
 * Определить, за какое время он одолел первую половину пути?
 */
fun timeForHalfWay(
    t1: Double, v1: Double,
    t2: Double, v2: Double,
    t3: Double, v3: Double
): Double {
    return when (val halfPath = (t1 * v1 + t2 * v2 + t3 * v3) / 2.0) {
        in 0.0..t1 * v1 -> halfPath / v1
        in t1 * v1..t1 * v1 + t2 * v2 -> t1 + (halfPath - t1 * v1) / v2
        else -> (halfPath - t1 * v1 - t2 * v2) / v3 + t1 + t2
    }

}

